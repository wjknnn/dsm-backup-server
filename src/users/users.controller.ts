@ApiTags('Auth')
@Controller('auth')
export class AuthController {
  constructor(private readonly userService: UsersService) {}

  @Post('signup')
  async signupUser(@Body() createUserDto: CreateUserDto) {
    return await this.userService.signupUser(createUserDto);
  }
}
